using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace pest_control
{
    public class Location
    {
        public int X
        {
            get { return x; }
            set { x = value; }
        }
        public int Y
        {
            get { return y; }
            set { y = value; }
        }

        private int x;
        private int y;

        public Location(int x, int y)
        {
            this.X = x;
            this.Y = y;
        }
    }
}
